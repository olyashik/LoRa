# ПАРАМЕТРЫ ТРАНСИВЕРА

SF             = 7        # Spreading Factor: 7..12
BW_KHZ         = 125      # Bandwidth: 125 / 250 / 500 кГц
CR             = 1        # Coding Rate: 1=4/5  2=4/6  3=4/7  4=4/8
TX_POWER_DBM   = 14.0     # Мощность передатчика, дБм 
FREQ_MHZ       = 868.1    # Несущая частота, МГц
PREAMBLE_SYMS  = 8        # Число символов преамбулы  (LoRaWAN стандарт: 8)
EXPLICIT_HDR   = True     # True = явный заголовок (стандарт LoRaWAN)


# ПАРАМЕТРЫ ПАКЕТА

PAYLOAD_BYTES  = 100       # Размер случайного payload, байт (1..255)
RANDOM_SEED    = None       # Сид генератора случайных бит.
                          # None → новый пакет при каждом запуске.
                          # Число → воспроизводимый результат.


# КАНАЛ: ВКЛЮЧЕНИЕ / ВЫКЛЮЧЕНИЕ ЭФФЕКТОВ

ENABLE_AWGN        = True   # Белый гауссовский шум (тепловой шум приёмника).
                             # False → идеальный канал без шума.

ENABLE_PATH_LOSS   = True   # Потери на распространение (модель log-distance).
                             # False → сигнал приходит без затухания;
                             #         используется FIXED_SNR_DB.

ENABLE_LDR_AUTO    = True   # Авто-включение Low Data Rate Optimize.
                             # False → LDR всегда выключен.


# ПАРАМЕТРЫ КАНАЛА (активны если соответствующий флаг = True)

DISTANCE_M         = 15000   # Расстояние TX→RX, метры
PATH_LOSS_EXP      = 2.7    # Показатель затухания n:
                             #   2.0 — свободное пространство
                             #   2.7 — типичная городская среда
                             #   3.5 — внутри здания
                             #   4.0 — сильно затенённая среда
NOISE_FIGURE_DB    = 6.0    # Шум-фигура приёмника, дБ
TEMPERATURE_K      = 290.0  # Температура для теплового шума, К  (290 К ≈ 17°C)

# Фиксированный SNR, если ENABLE_PATH_LOSS = False:
FIXED_SNR_DB       = 10.0   # дБ.  Примеры:
                             #   > 0   — хороший сигнал
                             #   -7.5  — у порога для SF=7
                             #   -20.0 — у порога для SF=12


# ПАРАМЕТРЫ ВЫВОДА

SHOW_BIT_MAP       = True   # Карта ошибочных бит ('.' верно, 'X' ошибка)
SHOW_BYTE_DIFF     = True   # Побайтовое сравнение TX vs RX
BIT_MAP_WIDTH      = 64     # Ширина карты в битах