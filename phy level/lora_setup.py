"""
lora_setup.py — все настройки в одном месте
"""

# ===========================================================================
# ПАРАМЕТРЫ ТРАНСИВЕРА (LoRaParams)
# ===========================================================================

SF             = 11        # Spreading Factor: 7..12
                          # ↑ выше SF → дальше, медленнее, устойчивее к шуму

BW_KHZ         = 125      # Bandwidth: 125 / 250 / 500 кГц
                          # ↑ шире BW → быстрее, но хуже чувствительность

CR             = 1        # Coding Rate: 1=4/5, 2=4/6, 3=4/7, 4=4/8
                          # ↑ выше CR → больше защита, медленнее

TX_POWER_DBM   = 14.0     # Мощность передатчика, дБм. EU868: макс 14 дБм

FREQ_MHZ       = 868.1    # Несущая частота, МГц

PREAMBLE_SYMS  = 8        # Число символов преамбулы. LoRaWAN стандарт: 8

EXPLICIT_HDR   = True     # True = явный заголовок (стандарт LoRaWAN)
                          # False = неявный (только если длина пакета известна)

# ===========================================================================
# ПАРАМЕТРЫ ПАКЕТА
# ===========================================================================

PAYLOAD_BYTES  = 16       # Размер случайного payload, байт (1..255)

RANDOM_SEED    = None       # Сид генератора случайных бит.
                          # None → новый случайный пакет при каждом запуске
                          # Число → воспроизводимый результат

# ===========================================================================
# КАНАЛ: ВКЛЮЧЕНИЕ / ВЫКЛЮЧЕНИЕ ЭФФЕКТОВ
# ===========================================================================

ENABLE_AWGN        = False   # Белый гауссовский шум (тепловой шум приёмника)
                             # False → идеальный канал без шума

ENABLE_PATH_LOSS   = True   # Потери на распространение (модель log-distance)
                             # False → сигнал приходит с исходной мощностью

ENABLE_LDR_AUTO    = True   # Авто-включение Low Data Rate Optimize
                             # False → LDR всегда выключен (для исследования влияния)

# ===========================================================================
# ПАРАМЕТРЫ КАНАЛА (активны только если соответствующий флаг = True)
# ===========================================================================

DISTANCE_M         = 2000   # Расстояние TX→RX, метры (для модели потерь)

PATH_LOSS_EXP      = 2.7    # Показатель затухания n:
                             # 2.0 = свободное пространство
                             # 2.7 = типичная городская среда
                             # 3.5 = внутри здания / плотная застройка
                             # 4.0 = сильно затенённая среда

NOISE_FIGURE_DB    = 6.0    # Шум-фигура приёмника, дБ.
                             # SX1276 типично: 6 дБ

TEMPERATURE_K      = 290.0  # Температура для расчёта теплового шума, К
                             # 290 К ≈ 17°C (стандартная ссылочная температура)

# Если ENABLE_PATH_LOSS = False, используется этот фиксированный SNR:
FIXED_SNR_DB       = 10.0   # дБ. Подбери под нужный режим работы:
                             # >0   → хороший сигнал
                             # -7.5 → у порога для SF=7
                             # -20  → у порога для SF=12

# ===========================================================================
# ПАРАМЕТРЫ ВЫВОДА
# ===========================================================================

SHOW_BIT_MAP       = True   # Показать карту ошибочных бит (визуально)
SHOW_BYTE_DIFF     = True   # Показать побайтовое сравнение TX vs RX
BIT_MAP_WIDTH      = 64     # Ширина карты в битах (для переноса строк)